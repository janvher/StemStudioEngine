function isLocalDevEnv(envContent) {
  if (!envContent) return false;
  // Check if REACT_APP_SERVER_HOST points to localhost
  const serverHostMatch = envContent.match(/REACT_APP_SERVER_HOST\s*=\s*(.+)/);
  if (serverHostMatch) {
    const serverHost = serverHostMatch[1].trim();
    return serverHost.includes("localhost") || serverHost.includes("127.0.0.1");
  }
  return false;
}

// Service health checks (only for local development)
function checkMongoDB() {
  try {
    execSync('mongosh --eval \'db.runCommand("ping")\' --quiet', {
      stdio: "pipe",
    });
    console.log(`  ${colors.green}[OK]${colors.reset} MongoDB is running`);
    return true;
  } catch {
    try {
      execSync("pgrep -x mongod", { stdio: "pipe" });
      console.log(
        `  ${colors.yellow}[WARN]${colors.reset} MongoDB process found but not responding`,
      );
    } catch {
      console.log(`  ${colors.red}[STOP]${colors.reset} MongoDB is not running`);
    }
    return false;
  }
}

function checkRedis() {
  try {
    execSync("redis-cli ping", { stdio: "pipe" });
    console.log(`  ${colors.green}[OK]${colors.reset} Redis is running`);
    return true;
  } catch {
    try {
      execSync("pgrep -x redis-server", { stdio: "pipe" });
      console.log(
        `  ${colors.yellow}[WARN]${colors.reset} Redis process found but not responding`,
      );
    } catch {
      console.log(`  ${colors.red}[STOP]${colors.reset} Redis is not running`);
    }
    return false;
  }
}

function checkLocalServices() {
  console.log("\nChecking local services...");
  const mongoOk = checkMongoDB();
  const redisOk = checkRedis();

  if (!mongoOk) {
    console.log(
      `\n${colors.yellow}Tip:${colors.reset} Start MongoDB with: ${colors.cyan}brew services start mongodb/brew/mongodb-community${colors.reset}`,
    );
  }
  if (!redisOk) {
    console.log(
      `${colors.yellow}Tip:${colors.reset} Start Redis with: ${colors.cyan}brew services start redis${colors.reset}`,
    );
  }

  return { mongoOk, redisOk };
}

function backupFile(filePath, backupPath) {
  if (fs.existsSync(filePath)) {
    const timestamp = new Date().toISOString().replace(/[:.]/g, "-");
    const backupWithTimestamp = backupPath.replace(
      ".backup",
      `.backup.${timestamp}`,
    );
    fs.copyFileSync(filePath, backupWithTimestamp);
    console.log(
      `  Backed up: ${path.basename(filePath)} -> ${path.basename(backupWithTimestamp)}`,
    );
    return backupWithTimestamp;
  }
  return null;
}

function setupWebEnv() {
  console.log("\nChecking client/.env...");

  if (fs.existsSync(WEB_ENV_PATH)) {
    const currentEnv = fs.readFileSync(WEB_ENV_PATH, "utf-8");

    if (isLocalDevEnv(currentEnv)) {
      console.log("  client/.env is already configured for local development.");
      return false;
    }

    console.log("  client/.env is configured for remote servers.");
    backupFile(WEB_ENV_PATH, WEB_ENV_BACKUP_PATH);
  } else {
    console.log("  client/.env does not exist.");
  }

  fs.writeFileSync(WEB_ENV_PATH, WEB_LOCAL_ENV);
  console.log("  Created client/.env with local development defaults.");
  return true;
}

function setupServerEnv() {
  console.log("\nChecking server/.env...");

  if (fs.existsSync(SERVER_ENV_PATH)) {
    console.log("  server/.env already exists.");
    return false;
  }

  fs.writeFileSync(SERVER_ENV_PATH, SERVER_LOCAL_ENV);
  console.log("  Created server/.env with local development defaults.");
  return true;
}

function setupBuildServerFiles() {
  console.log("\nChecking build/server files...");

  // Ensure build/server directory exists
  if (!fs.existsSync(BUILD_SERVER_DIR)) {
    fs.mkdirSync(BUILD_SERVER_DIR, { recursive: true });
    console.log("  Created build/server directory.");
  }

  let envChanged = false;
  let configChanged = false;

  // Sync server/.env to build/server/.env
  if (fs.existsSync(SERVER_ENV_PATH)) {
    const serverEnv = fs.readFileSync(SERVER_ENV_PATH, "utf-8");
    const buildEnvExists = fs.existsSync(BUILD_SERVER_ENV_PATH);
    const buildEnv = buildEnvExists
      ? fs.readFileSync(BUILD_SERVER_ENV_PATH, "utf-8")
      : "";

    if (serverEnv !== buildEnv) {
      if (buildEnvExists) {
        backupFile(BUILD_SERVER_ENV_PATH, BUILD_SERVER_ENV_PATH + ".backup");
      }
      fs.writeFileSync(BUILD_SERVER_ENV_PATH, serverEnv);
      console.log("  Synced server/.env -> build/server/.env");
      envChanged = true;
    } else {
      console.log("  build/server/.env is up to date.");
    }
  }

  // Create/update build/server/config.toml from config-dev.toml
  if (fs.existsSync(SERVER_CONFIG_DEV_PATH)) {
    const devConfig = fs.readFileSync(SERVER_CONFIG_DEV_PATH, "utf-8");

    if (!fs.existsSync(BUILD_SERVER_CONFIG_PATH)) {
      fs.writeFileSync(BUILD_SERVER_CONFIG_PATH, devConfig);
      console.log("  Created build/server/config.toml from config-dev.toml");
      configChanged = true;
    } else {
      console.log("  build/server/config.toml already exists.");
    }
  } else {
    console.log(
      `  ${colors.yellow}Warning:${colors.reset} server/config-dev.toml not found.`,
    );
  }

  return envChanged || configChanged;
}

function main() {
  console.log("===========================================");
  console.log("  Setting up local development environment");
  console.log("===========================================");

  // Check local services first
  const { mongoOk, redisOk } = checkLocalServices();

  const webChanged = setupWebEnv();
  const serverChanged = setupServerEnv();
  const buildChanged = setupBuildServerFiles();

  console.log("\n-------------------------------------------");
  if (webChanged || serverChanged || buildChanged) {
    console.log("Local environment setup complete!");
    console.log("\nNote: Some API keys are empty. Add your own keys to:");
    console.log("  - client/.env (for frontend features)");
    console.log("  - server/.env (for backend AI features)");
    console.log("  - build/server/.env (synced from server/.env)");
  } else {
    console.log("Environment already configured for local development.");
  }

  // Show service status summary
  console.log("\nService Status:");
  console.log(
    `  MongoDB: ${mongoOk ? colors.green + "Ready" : colors.red + "Not running"}${colors.reset}`,
  );
  console.log(
    `  Redis: ${redisOk ? colors.green + "Ready" : colors.yellow + "Not running (optional)"}${colors.reset}`,
  );

  if (!mongoOk) {
    console.log(
      `\n${colors.yellow}Warning:${colors.reset} MongoDB is required for the backend server.`,
    );
  }

  console.log("-------------------------------------------\n");
}

main();
